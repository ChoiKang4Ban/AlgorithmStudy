import java.io.*;
import java.util.*;

public class Main_1411 {

    static int n;
    static String arr[];
    static BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
    public static void main(String[] args) throws IOException {

        n = Integer.parseInt(br.readLine());
        arr = new String[n];

        for(int i=0;i<n;i++){

            arr[i] = br.readLine();

        }

    }
}
